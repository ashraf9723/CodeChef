# Read the number of test cases
T = int(input())

# Iterate through each test case
for _ in range(T):
    # Read the number of sets for the current test case
    X = int(input())
    
    # Calculate the total number of squats for this test case
    total_squats = X * 15
    
    # Print the total number of squats for this test case
    print(total_squats)
